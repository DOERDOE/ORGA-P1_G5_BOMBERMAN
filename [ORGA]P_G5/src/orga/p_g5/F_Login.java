    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        //administrador
        String usuario=jTextField1.getText();
        String Password=jPasswordField1.getText();
        if(usuario.equals("ADMIN") && Password.equals("ORGA_DIC_2019")){
            F_Administrador F_Admin=new F_Administrador();
            JOptionPane.showMessageDialog(null, "Se ha ingresado como Administrador Correctamente");
            F_Admin.show();
            this.dispose();
            F_Admin.setLocationRelativeTo(null);
            F_Admin.Usu_P=this.Usu_P;
            F_Admin.Usuarios=this.Usuarios;
            
            
        }else{
            //usuarios
            for (int x = 0; x < Usuarios.size(); x++) {
                Lista_Usuario Usu = Usuarios.get(x);
                if(Usu.getUsuario().equals(usuario)&&Usu.getPass().equals(Password)){
                    F_Usuario F_Usu=new F_Usuario();
                    F_Usu.Usuarios=this.Usuarios;
                    F_Usu.Usu_P=this.Usu_P;
                    F_Usu.jLabel2.setText(usuario);
                    JOptionPane.showMessageDialog(null, "Se ha ingresado como Usuario Correctamente");
                    F_Usu.show();
                    F_Usu.setLocationRelativeTo(null);
                    this.dispose();
                    
                    
                }
            }
            jTextField1.setText("");
            jPasswordField1.setText("");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

